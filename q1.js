/* Document Object:

Represents the structure and content of a web page.
Provides methods to manipulate HTML elements and their attributes.
Organizes elements in a hierarchical tree structure (DOM).

Window Object:

Represents the browser window or tab.
Serves as the global context for JavaScript code.
Provides methods to interact with the browser environment and control window behavior.


Relationship:

Document is a property of Window (window.document).
Window methods affect the Document object or its elements.*/